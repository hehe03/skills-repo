def _rule_conditions(rule: Dict[str, Any]) -> Iterable[Dict[str, Any]]:
    for key in ("all", "any"):
        conditions = rule.get(key) or []
        if isinstance(conditions, list):
            for condition in conditions:
                if isinstance(condition, dict):
                    yield condition


def infer_feature_group(rule: Dict[str, Any]) -> str:
    groups: list[str] = []
    for condition in _rule_conditions(rule):
        feature = str(condition.get("feature", ""))
        if feature.startswith("field_") or feature == "trace_field_paths":
            groups.append("dynamic_fields")
        elif feature in FEATURE_GROUP_BY_FEATURE:
            groups.append(FEATURE_GROUP_BY_FEATURE[feature])
    if not groups:
        return str(rule.get("feature_group") or "unknown")
    priority = [
        "final_answer",
        "error_signal",
        "result_quality",
        "loop_repetition",
        "behavior",
        "schema_profile",
        "dynamic_fields",
        "structure",
    ]
    for item in priority:
        if item in groups:
            return item
    return groups[0]


def infer_source_method(rule: Dict[str, Any]) -> str:
    if rule.get("source_method"):
        return str(rule["source_method"])
    layer = str(rule.get("layer", "")).lower()
    if layer == "general":
        return "non_llm_no_train"
    if layer == "unlabeled":
        return "non_llm_unlabeled"
    if layer == "labeled":
        return "non_llm_labeled"
    if layer == "llm":
        return "llm"
    return layer or "unknown"


def infer_component(rule: Dict[str, Any]) -> str:
    if rule.get("component"):
        return str(rule["component"])

    layer = str(rule.get("layer", "")).lower()
    rule_id = str(rule.get("id", "")).lower()
    group = str(rule.get("group", "")).lower()
    feature_group = infer_feature_group(rule)

    if layer == "general":
        if group == "hard_failure" or rule_id in {"parse_error", "empty_trace"}:
            return "static_hard_failure"
        if feature_group == "final_answer":
            return "static_final_answer"
        if feature_group == "error_signal":
            return "static_error_signal"
        if feature_group == "result_quality":
            return "static_result_quality"
        if feature_group == "loop_repetition":
            return "static_loop_repetition"
        if feature_group == "structure":
            return "static_structure"
        if str(rule.get("label")) == "goodcase":
            return "static_good_support"
        return "custom_rules"

    if layer == "unlabeled":
        if feature_group == "final_answer":
            return "static_final_answer"
        if feature_group == "behavior":
            return "unlabeled_behavior_quantile"
        if feature_group == "schema_profile":
            return "unlabeled_schema_profile"
        if feature_group == "dynamic_fields":
            if "stats" in rule_id or "unique_ratio" in rule_id:
                return "unlabeled_field_stats"
            return "unlabeled_field_presence"
        return "unlabeled_numeric_quantile"

    if layer == "labeled":
        if feature_group == "final_answer":
            return "labeled_final_answer_diff"
        if feature_group == "behavior":
            return "labeled_behavior_diff"
        if feature_group == "schema_profile":
            return "labeled_schema_profile"
        if feature_group == "dynamic_fields":
            if "stats" in rule_id or any(token in rule_id for token in ("text_chars", "unique", "empty_count", "error_text", "range", "ratio")):
                return "labeled_field_stats"
            if "value" in rule_id:
                return "labeled_field_value"
            if "number" in rule_id or "numeric" in rule_id:
                return "labeled_field_numeric"
            return "labeled_field_presence"
        return "labeled_numeric_diff"

    if layer == "llm" or infer_source_method(rule).startswith("llm"):
        return "llm_rules"

    return "custom_rules"


def annotate_rule(rule: Dict[str, Any]) -> Dict[str, Any]:
    annotated = dict(rule)
    annotated.setdefault("component", infer_component(rule))
    annotated.setdefault("feature_group", infer_feature_group(rule))
    annotated.setdefault("source_method", infer_source_method(rule))
    return annotated
